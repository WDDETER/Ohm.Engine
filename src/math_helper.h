#ifndef _OHM_MATH_HELPER_H
#define _OHM_MATH_HELPER_H


#define PIF 3.14159f
#define PID 3.14159

#define RADF_TO_DEGF(x) (x * (PIF / 180.0f))
#define DEGF_TO_RADF(x) (x * (180.0f / PIF))
#define RAD_TO_DEG(x)   (x * (PID / 180.0))
#define DEG_TO_RAD(x)   (x * (180.0 / PID))


#endif